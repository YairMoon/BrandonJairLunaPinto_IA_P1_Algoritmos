# Representamos una situación como un diccionario (marco)
marco_ir_al_super = {
    "acción": "Ir al supermercado",
    "actor": "Pedro",
    "ubicación": "Supermercado",
    "objetivo": "Comprar leche"
}

# Imprimimos la acción descrita en el marco
print(f"{marco_ir_al_super['actor']} va al {marco_ir_al_super['ubicación']} para {marco_ir_al_super['objetivo']}.")
# Salida: Pedro va al supermercado para Comprar leche.
# En este ejemplo, el marco describe la acción de ir al supermercado, el actor que realiza la acción (Pedro), la ubicación (Supermercado) y el objetivo de la acción (Comprar leche).